-- 통계 : 소계와 총계 - UNION ALL로 작성해 보자
-- 시나리오 : 
--	출력 SELECT
--	UNOIN ALL
--	소계	SUM
--	UNOIN ALL
--	출력
--	UNOIN ALL
--	소계
--		총계
-- Q. 부서 + 업무별 "개수-COUNT" "급여합-SUM" 통계
SELECT DNAME, JOB, COUNT(*) "DNAME+JOB", SUM(SAL) "DNAME+JOB 급여합"
	FROM EMP e JOIN DEPT d
	USING(DEPTNO)
	GROUP BY DNAME, JOB
UNION ALL
-- 부서 개수 급여합
SELECT DNAME, ' ', COUNT(*), SUM(SAL)
	FROM EMP e JOIN DEPT d
	USING(DEPTNO)
	GROUP BY DNAME
UNION ALL
-- 업무 개수 급여합
SELECT ' ', JOB, COUNT(*), SUM(SAL)
	FROM EMP e JOIN DEPT d
	USING(DEPTNO)
	GROUP BY JOB
UNION ALL
-- 전체총계
SELECT '전체', ' ', COUNT(*), SUM(SAL)
	FROM EMP
ORDER BY 1 , 2;

