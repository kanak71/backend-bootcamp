 --GROUP BY는 작성에는 순서가 없고, ROLLUP의 컬럼이 통계의 대상이 된다
SELECT DNAME, JOB, COUNT(*), SUM(SAL)
 	FROM EMP e JOIN DEPT d
 	USING(DEPTNO)
	GROUP BY ROLLUP(JOB), DNAME
	ORDER BY 1, 2;
 
 --------------------- GROUPING은 GROUP되어 있는 항목을 값으로 처리해줌
 -- ROLLUP으러 생성된 컬럼의 경우 1, 아닌 경우 0
 -- 결론  JOB으로만 통계가 발생 GROUPING(DNAME) : 0 , GROUPING(JOB) : 1
 --		 DNAME(상위) 통계가 발생 GROUPING(DNAME) : 1 , GROUPING(JOB) : 1
SELECT DNAME, JOB, 
 	GROUPING(DNAME), GROUPING(JOB),
 	COUNT(*), SUM(SAL)
 	FROM EMP e JOIN DEPT d
 	USING(DEPTNO)
 	GROUP BY ROLLUP(DNAME, JOB)
 	ORDER BY 1;

 -- GROUPING으로 나온 0과 1의 값을 CASE문을 통해 사용자가 쉽게 알아볼 수 있도록 만들자
 SELECT --DNAME, JOB, 
 	CASE GROUPING(DNAME) WHEN 1 THEN '모든부서' ELSE DNAME END AS 부서 , 
 	CASE GROUPING(JOB) WHEN 1 THEN '모든업무' ELSE JOB END AS 업무, 
 	COUNT(*), SUM(SAL)
 	FROM EMP e JOIN DEPT d
 	USING(DEPTNO)
 	GROUP BY ROLLUP(DNAME, JOB)
 	ORDER BY 1, 2;
 
