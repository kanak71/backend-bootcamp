-- GROUP BY와 통계함수의 차이점
-- 결론 : 작성하는 순서에 영향을 받는다
-- Q. 부서명(DNAME)과 업무명(JOB)을 기준으로 사원수와 급여 합을 집계
SELECT DNAME, JOB
	FROM EMP e JOIN DEPT d
	USING(DEPTNO)
	GROUP BY DNAME, JOB
	ORDER BY 1;

SELECT DNAME, JOB
	FROM EMP e JOIN DEPT d
	USING(DEPTNO)
	GROUP BY JOB, DNAME
	ORDER BY 1;

