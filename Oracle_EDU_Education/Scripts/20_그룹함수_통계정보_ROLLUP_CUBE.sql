-- CUBE 다차원 통계 : 발생할 수 있는 모든 소계, 총계에 대한 정보를 출력해 준다
-- ROLLUP(DNAME, JOB) - DNAME 통계 , DNAME+JOB 통계
-- CUBE(DNAME, JOB) - DNAME 통계 , JOB 통계 , DNAME+JOB 통계

SELECT  DNAME, JOB,  COUNT(*), SUM(SAL)
	FROM EMP e JOIN DEPT d 
	USING(DEPTNO)
	GROUP BY ROLLUP(DNAME, JOB)
	ORDER BY DNAME, JOB ; 	-- 13개 : 1줄 ACCOUNTING, RESEARCH, SALES = 전체 통계 14/22400
--										1줄 ACCOUNTING
--										1줄 RESEARCH
--										1줄 SALES
--										각 ACCOUNTING 3줄, RESERACH 3줄, SALES 3줄
	
SELECT  DNAME, JOB,  COUNT(*), SUM(SAL)
	FROM EMP e JOIN DEPT d 
	USING(DEPTNO)
	GROUP BY CUBE(DNAME, JOB)
	ORDER BY DNAME, JOB ;

-- GROUPING SETS(컬럼) : 작성한 컬럼만 통계를 작성 하여 출력
-- CVAS를 통해서 많이 사용하는 SQL을 메소드처럼 만들어서 사용한다 => VIEW

CREATE VIEW EMPNDEPT AS (SELECT DNAME, JOB, SAL
							FROM EMP e JOIN DEPT d
							USING(DEPTNO));
	
SELECT *
	FROM EMPNDEPT e ;

SELECT JOB, COUNT(*), SUM(SAL)
	FROM EMPNDEPT e 
	GROUP BY GROUPING SETS(JOB);

SELECT DNAME, JOB, COUNT(*), SUM(SAL)
	FROM EMPNDEPT e 
	GROUP BY GROUPING SETS(JOB, DNAME);	--DNAME 3개, JOB 5개
	
-- GROUP BY ROLLUP, CUBE, GROUPING SETS를 UNION ALL을 통해서 작성해라
-- 4개의 컬럼
-- DNAME, JOB, COUNT(*), SUM(SAL)
-- 1단계 DNAME별의 소계
SELECT DNAME, '',COUNT(*), SUM(SAL)
	FROM EMPNDEPT e
	GROUP BY DNAME
UNION ALL
-- 2단계 JOB의 소계
SELECT '', JOB, COUNT(*), SUM(SAL)
	FROM EMPNDEPT e 
	GROUP BY JOB;
 











